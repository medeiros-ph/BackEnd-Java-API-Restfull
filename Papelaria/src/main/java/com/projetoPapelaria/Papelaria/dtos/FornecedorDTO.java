package com.projetoPapelaria.Papelaria.dtos;

public class FornecedorDTO {

	private Long idFornecedor;
	private String nomeFornecedor;
	private String cnpjFornecedor;
	private String enderecoFornecedor;
	private String contatoFornecedor;
	private String imagemFornecedor;
	private Boolean statusFornecedor;
	
	
	public Long getIdFornecedor() {
		return idFornecedor;
	}
	public void setIdFornecedor(Long idFornecedor) {
		this.idFornecedor = idFornecedor;
	}
	public String getNomeFornecedor() {
		return nomeFornecedor;
	}
	public void setNomeFornecedor(String nomeFornecedor) {
		this.nomeFornecedor = nomeFornecedor;
	}
	public String getCnpjFornecedor() {
		return cnpjFornecedor;
	}
	public void setCnpjFornecedor(String cnpjFornecedor) {
		this.cnpjFornecedor = cnpjFornecedor;
	}
	public String getEnderecoFornecedor() {
		return enderecoFornecedor;
	}
	public void setEnderecoFornecedor(String enderecoFornecedor) {
		this.enderecoFornecedor = enderecoFornecedor;
	}
	public String getContatoFornecedor() {
		return contatoFornecedor;
	}
	public void setContatoFornecedor(String contatoFornecedor) {
		this.contatoFornecedor = contatoFornecedor;
	}
	public String getImagemFornecedor() {
		return imagemFornecedor;
	}
	public void setImagemFornecedor(String imagemFornecedor) {
		this.imagemFornecedor = imagemFornecedor;
	}
	public Boolean getStatusFornecedor() {
		return statusFornecedor;
	}
	public void setStatusFornecedor(Boolean statusFornecedor) {
		this.statusFornecedor = statusFornecedor;
	}
	
	
}
